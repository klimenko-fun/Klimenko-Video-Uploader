package io.github.klimenko.videouploader.jfxExtension;

public class WindowFrame {
    public WindowFrame(double x, double y, double width, double height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public double x;
    public double y;
    public double width;
    public double height;
}
